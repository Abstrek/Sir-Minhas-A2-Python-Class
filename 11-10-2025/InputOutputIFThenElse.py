x = int(input("Enter a number: "))
y = int(input("Enter a second number: "))
sign = input("Enter an arthimetic operator: ")

if(sign == "+"):
    print(x+y)
elif(sign == "-"):
    print(x-y)
elif(sign == "/"):
    print(x/y)
elif(sign == "*"):
    print(x*y)
else: 
    print("invalid operator")






